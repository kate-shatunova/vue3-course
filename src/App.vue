<template>
  <div>Root component start!</div>
  <button @click="addLike">Like</button>
  <button @click="addDislike">Dislike</button>
  <div>Count likes: <strong>{{ likes }}</strong></div>
  <div>Count dislikes: <strong>{{ dislikes }}</strong></div>
</template>

<script>
export default {
  name: "App",
  data() {
    return {
      likes: 0,
      dislikes: 0
    }
  },
  methods: {
    addLike() {
      this.likes += 1;
    },
    addDislike() {
      this.dislikes += 1;
    }
  }
}
</script>

